#[derive(Debug, Clone, PartialEq)]
pub enum Event
{
    Keyboard(EvState, Key),
    MouseButton(EvState, Button, (f64, f64)),
    MouseMoved(f64, f64),
    AxisMoved(AxisState),
    PointerInside(bool),
    Resized(u32, u32),
    Moved(i32, i32),
    Focused(bool),
    Redraw,
    CloseButton,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum Button
{
    Left,
    Middle,
    Right,
    Other(u8),
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum EvState
{
    Pressed,
    Released,
}

#[derive(Debug, Clone, Copy, PartialEq)]
pub enum AxisState
{
    Scroll(f64, f64),
    Pressure(f64),
    Tilt(f64, f64),
}

